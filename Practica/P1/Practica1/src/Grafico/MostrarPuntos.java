    public MostrarPuntos(java.awt.Frame parent, boolean modal,String opc) {
        super(parent, modal);
        initComponents();
        String s[] = {"ID", "X", "Y"};
        dtm.setColumnIdentifiers(s);
        if(opc.equals("MenuA"))
              puntos = Menu_PA.puntosP;
        else
            puntos = Menu_PB.puntosP;
        
        if (puntos != null) {
            for (Punto punto : puntos) {
                dtm.addRow(new Object[]{punto.getId(), punto.getX(), punto.getY()});
            }

            jTable1.setModel(dtm);
            this.setVisible(true);
        } else {
            this.setVisible(false);
            JOptionPane.showMessageDialog(null, "Deberias Cargar puntos en la memoria primero.");
            
        }
        
    }
