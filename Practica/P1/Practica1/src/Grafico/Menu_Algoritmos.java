    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Menu_PA.puntosP != null) {
            if (!eleccion.getSelectedItem().equals("--")) {
                switch (eleccion.getSelectedItem()) {
                    case "Exhaustivo":
                        l = Algoritmo.Exhaustivo(Menu_PA.puntosP, 0, Menu_PA.puntosP.length - 1);
                        break;
                    case "Poda":
                        l = Algoritmo.Bpoda(Menu_PA.puntosP, Menu_PA.puntosP.length - 1);
                        break;
                    case "Divide y Venceras":
                        Algoritmo.quick_sort(Menu_PA.puntosP, 0, Menu_PA.puntosP.length - 1);
                        l = Algoritmo.DyV(Menu_PA.puntosP, 0, Menu_PA.puntosP.length - 1, 0);
                        break;
                    case "DyV Mejorado":
                        Algoritmo.quick_sort(Menu_PA.puntosP, 0, Menu_PA.puntosP.length - 1);
                        l = Algoritmo.AlgoritmoDyVMejorado(Menu_PA.puntosP, 0, Menu_PA.puntosP.length - 1, 0);
                        break;
                }
                jTextField6.setText(l.getP1().toString());
                jTextField7.setText(l.getP2().toString());
                jTextField3.setText("" + l.distancia());
            } else {
                JOptionPane.showMessageDialog(null, "Deberias Elige el algoritmo primero.");

            }
        } else {
            JOptionPane.showMessageDialog(null, "La memoria esta vacia.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

