    private void MostrarGrafica(String selectedItem) {

        funcion = new double[10];
        y = new double[10];
        funcion2 = new double[10];
        y2 = new double[10];
        switch (selectedItem) {
            case "Exhaustivo":
                for (int i = 1; i <= 10; i++) {
                    int x = i * 100;
                    funcion[i - 1] = 5 * pow(x, 2) + 10 * x + 9;
                    funcion2[i-1]= 10 * pow(x, 2) + 5 * x + 9;
                    y[i - 1] = x;
                }
                break;

            case "Poda":
                for (int i = 1; i <= 10; i++) {
                    int x = i * 100;
                    funcion[i - 1] = (3 / 2) * pow(x, 2) - x + x * Math.log(x) + 15;
                    funcion2[i-1]= 8 * pow(x, 2) + 2 * x +x * Math.log(x)+ 19;
                    y[i - 1] = x;
                }
                break;
            case "Divide y Venceras":

                for (int i = 1; i <= 10; i++) {
                    int x = i * 100;
                    funcion[i - 1] = 2 * funcion[(i - 1) / 2] + 4 * pow(x, 2) + 11 * x + 49;
                    funcion2[i-1]=2 * funcion[(i - 1) / 2] + 9 * pow(x, 2) + 6 * x + 55;
                    y[i - 1] = x;
                }
                break;
            case "DyV Mejorado":
                for (int i = 1; i <= 10; i++) {
                    int x = i * 100;
                    funcion[i - 1] = 2 * funcion[(i - 1) / 2] + 4 * pow(x, 2) + 11 * x + 49;
                    funcion2[i-1]=2 * funcion[(i - 1) / 2] + 9 * pow(x, 2) + 6 * x + 55;
                    y[i - 1] = x;
                }

                break;
        }
        y2=y;
        Grafica_Algoritmos g = new Grafica_Algoritmos(new javax.swing.JFrame(), true);
        g.add(g.paint(funcion, y, funcion2, y2));
        g.setVisible(true);
    }
