    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (jTextField6.getText() != null && (!automata.getTransicionesλ().isEmpty() || !automata.getTransiciones().isEmpty())) {
            String[] aux;
            String v3 = jTextField7.getText();
            aux = v3.split(",");
            int[] fin = new int[aux.length];
            for (int i = 0; i < aux.length; i++) {
                String[] r = aux[i].split("q");
                fin[i] = Integer.parseInt(r[1]);
            }
            automata.setEstadosFinales(fin);
            try {
                fich.CreaFich(automata, jTextField6.getText());
            } catch (IOException ex) {
                Logger.getLogger(CrearAFND.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextField7.setText("");
            jTextField6.setText("");
            this.dispose();
            v.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "debe rellenar todos los campos.");
            this.dispose();
            v.setVisible(true);
        }
        

    }//GEN-LAST:event_jButton2ActionPerformed

