    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText() != null && jTextField2.getText() != null && jTextField3.getText() != null) {
            String v1 = jTextField1.getText();
            String[] aux = v1.split("q");
            int ini = Integer.parseInt(aux[1]);
            
            String v2 = jTextField2.getText();
            
            String v3 = jTextField3.getText();
            aux = v3.split(",");
            int[] fin = new int[aux.length];
            for (int i = 0; i < aux.length; i++) {
                String[] r = aux[i].split("q");
                fin[i] = Integer.parseInt(r[1]);
            }
            
            automata.agregarTransicion(ini, v2.charAt(0), fin);
        } else {
            JOptionPane.showConfirmDialog(rootPane, "Error debes rellenar todos los campos.");
        }
        vaciarDatos();
    }//GEN-LAST:event_jButton1ActionPerformed

