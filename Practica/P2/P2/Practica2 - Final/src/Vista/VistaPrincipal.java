    private void BotonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearActionPerformed
        String tipoGrafo = (String) tipoGraf.getSelectedItem();
        System.out.println("Tipo de grafo elejido: " + tipoGrafo);
        if (tipoGrafo.equals("AFD")) {
            CrearAFD afd = new CrearAFD(this);
            this.dispose();
            afd.setVisible(true);
        } else {
            CrearAFND afnd = new CrearAFND(this);
            this.dispose();
            afnd.setVisible(true);
        }
        NombreFich.setText("");
    }//GEN-LAST:event_BotonCrearActionPerformed

    private void botonCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCargarActionPerformed
        if(NombreFich.getText()!=null){
            String nombreFich = NombreFich.getText();
        System.out.println("Cargando fichero " + nombreFich + "...");
        Object devolver=fich.leerFichero(new File("Ficheros\\"+nombreFich+".txt"));
        
        Menu m = new Menu(this,devolver);
        
        m.setVisible(true);
        this.dispose();
         NombreFich.setText("");
        }else{
            JOptionPane.showConfirmDialog(rootPane, "Debes rellernar dicho campo.");
        }
        
    }//GEN-LAST:event_botonCargarActionPerformed

